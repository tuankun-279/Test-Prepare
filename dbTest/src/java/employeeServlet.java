    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        EntityManager em = Persistence.createEntityManagerFactory("dbTestPU").createEntityManager();
        String id = request.getParameter("id");
        if(id != null && !id.isEmpty()){
            Tblemployee emp = em.find(Tblemployee.class, id);
            
            em.getTransaction().begin();
            em.remove(emp);
            em.getTransaction().commit();
        }
         List<Tblemployee> list = em.createNamedQuery("Tblemployee.findAll", Tblemployee.class).getResultList();
         request.setAttribute("emlist", list);
         request.getRequestDispatcher("display.jsp").forward(request, response);
    }

   
